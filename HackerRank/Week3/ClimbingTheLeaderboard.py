def climbingLeaderboard(ranked, player):
    unique = sorted(list(set(ranked)), reverse=True)
    res= []
    index= len(unique)-1
    for marks in player:
        while index>=0 and marks >= unique[index]:
            index-=1
        res.append(index+2)
    return res    
